class StoryManager {
    constructor() {
        this.currentChapter = 0;
        this.triggeredEvents = new Set();
        this.newsHistory = [];
    }

    getIntro() {
        return STORY_CONTENT.intro;
    }

    getPathogenStory(pathogenType) {
        return STORY_CONTENT.pathogen[pathogenType] || STORY_CONTENT.pathogen.bacteria;
    }

    getCountryStory(countryId) {
        return STORY_CONTENT.countries[countryId];
    }

    getEventMessage(eventType, subType, countryName = '') {
        const events = STORY_CONTENT.events[eventType];
        if (!events) return '';
        
        const subEvents = events[subType];
        if (!subEvents) return '';
        
        const messages = subEvents.messages || [];
        const message = messages[Math.floor(Math.random() * messages.length)];
        
        return message.replace('{country}', countryName || '该国');
    }

    getNewsHeadline(isPanic = false) {
        const headlines = isPanic ? STORY_CONTENT.news.panic.headlines : STORY_CONTENT.news.headlines;
        const headline = headlines[Math.floor(Math.random() * headlines.length)];
        
        this.newsHistory.push(headline);
        if (this.newsHistory.length > 50) {
            this.newsHistory.shift();
        }
        
        return headline;
    }

    getEnding(type) {
        return STORY_CONTENT.endings[type] || STORY_CONTENT.endings.stalemate;
    }

    getRandomTip() {
        const tips = STORY_CONTENT.tips;
        return tips[Math.floor(Math.random() * tips.length)];
    }

    calculateVictoryType(deadRatio, infectedRatio, turn) {
        if (infectedRatio >= 0.99) {
            return 'total_victory';
        } else if (deadRatio >= 0.5) {
            return 'partial_victory';
        } else if (turn > 500) {
            return 'stalemate';
        } else {
            return 'defeat';
        }
    }
}

if (typeof module !== 'undefined' && module.exports) {
    module.exports = { STORY_CONTENT, StoryManager };
}
